const mongoose = require("mongoose");

// ---------------Model for 'users'----------------

const loginSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("user", loginSchema);
